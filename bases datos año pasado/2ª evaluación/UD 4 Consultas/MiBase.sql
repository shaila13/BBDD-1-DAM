
DROP TABLE CLIENTES CASCADE CONSTRAINTS;

CREATE TABLE CLIENTES(
  NUM_CLIENTE NUMBER(4),
  EMPRESA VARCHAR2(20 BYTE),  
  VENDEDOR_CLIENTE NUMBER(3),
  LIMITE_CREDITO NUMBER(6)
  ) 
TABLESPACE USERS LOGGING NOCOMPRESS NOCACHE NOPARALLEL MONITORING;


DROP TABLE OFICINAS CASCADE CONSTRAINTS;

CREATE TABLE OFICINAS( 
  OFICINA NUMBER(2),
  CIUDAD VARCHAR2(15 BYTE),
  REGION VARCHAR2(10 BYTE) DEFAULT 'Este',
  DIRECTOR NUMBER(3),
  OBJETIVO NUMBER(6),
  VENTAS NUMBER(6) DEFAULT 0
) 
TABLESPACE USERS LOGGING NOCOMPRESS NOCACHE NOPARALLEL MONITORING;



DROP TABLE PEDIDOS CASCADE CONSTRAINTS;

CREATE TABLE PEDIDOS(
 NUM_PEDIDO NUMBER(6),
 FECHA_PEDIDO DATE,
 CLIENTE NUMBER(4),
 VENDEDOR NUMBER(3),
 FABRICANTE VARCHAR2(3 BYTE),
 PRODUCTO VARCHAR2(5 BYTE),
 CANTIDAD NUMBER(3),
 IMPORTE NUMBER(6),
 ESTADO VARCHAR2(15)
)
TABLESPACE USERS LOGGING NOCOMPRESS NOCACHE NOPARALLEL MONITORING;



DROP TABLE PRODUCTOS CASCADE CONSTRAINTS;

CREATE TABLE PRODUCTOS( 
   ID_FAB VARCHAR2(3 BYTE), 
   ID_PRODUCTO VARCHAR2(5 BYTE),
   DESCRIPCION VARCHAR2(25 BYTE),
   PRECIO NUMBER(4),
   EXISTENCIAS NUMBER(3) DEFAULT 0,
   ID_PROV NUMBER(3)  
)
TABLESPACE USERS LOGGING NOCOMPRESS NOCACHE NOPARALLEL MONITORING;



DROP TABLE PROVEEDORES CASCADE CONSTRAINTS;

CREATE TABLE PROVEEDORES( 
  ID_PROV NUMBER(3),
  EMPRESA VARCHAR2(20 BYTE),  
  TIPO VARCHAR2(10),  
  TELEFONO VARCHAR2(9),
  MAIL VARCHAR2(20)
  )
  TABLESPACE USERS LOGGING NOCOMPRESS NOCACHE NOPARALLEL MONITORING;


DROP TABLE VENDEDORES CASCADE CONSTRAINTS;

CREATE TABLE VENDEDORES( 
  NUM_EMPLE NUMBER(3) NOT NULL, 
  NOMBRE VARCHAR2(15 BYTE), 
  EDAD NUMBER(2), 
  OFICINA_VEND NUMBER(2),
  TITULO VARCHAR2(15),
  FECHA_CONTRATO DATE, 
  DIRECTOR NUMBER(3),   
  CUOTA NUMBER(6), 
  VENTAS NUMBER(6)  
)
TABLESPACE USERS LOGGING NOCOMPRESS NOCACHE NOPARALLEL MONITORING;



