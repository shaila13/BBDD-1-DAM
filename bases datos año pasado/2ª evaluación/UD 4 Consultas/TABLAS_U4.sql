DROP TABLE EMPLE CASCADE CONSTRAINTS;

CREATE TABLE EMPLE ( 
  EMP_NO NUMBER(4) NOT NULL, 
  APELLIDO VARCHAR2(10 BYTE), 
  OFICIO VARCHAR2(10 BYTE), 
  DIR NUMBER(4), 
  FECHA_ALT DATE, 
  SALARIO NUMBER(10), 
  COMISION NUMBER(10), 
  DEPT_NO NUMBER(2) NOT NULL 
) TABLESPACE USERS LOGGING NOCOMPRESS NOCACHE NOPARALLEL MONITORING;


DROP TABLE LIBRERIA CASCADE CONSTRAINTS;

CREATE TABLE LIBRERIA ( 
  TEMA VARCHAR2(15 BYTE) NOT NULL, 
  ESTANTE VARCHAR2(1 BYTE), 
  num_ejemplares NUMBER(2) 
) 
TABLESPACE USERS LOGGING NOCOMPRESS NOCACHE NOPARALLEL MONITORING;


